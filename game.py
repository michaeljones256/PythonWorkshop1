class Game:
    def __init__ (self,name, description, characters):
        self.gameName = name
        self.gameDescription = description
        self.character = characters
    def attack(defender):
        defender.hp = defender.hp - randint(5,30)
    def restore(character):
        character.hp = character.hp + randint(1,10)
        if character.hp > 100:
            character.hp = 100

        

    

